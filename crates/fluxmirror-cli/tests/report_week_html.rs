#[test]
fn english_snapshot_hash_is_stable() {
    let snap_path = snapshot_path("week_html_en.html");
    let bytes = std::fs::read(&snap_path).expect("snapshot must exist for hash check");
    let digest = sha256_hex(&bytes);
    // The hash here is derived from the committed snapshot bytes — any
    // intentional renderer change updates BOTH the .html file AND this
    // string in the same commit.
    let expected = include_str!("snapshots/week_html_en.sha256").trim();
    assert_eq!(
        digest, expected,
        "english snapshot SHA-256 drift — \
         update tests/snapshots/week_html_en.sha256 if the renderer change is intended"
    );
}

#[test]
fn cli_format_html_writes_doctype_to_stdout() {
    // End-to-end: spawn the binary against an empty DB, ask for HTML,
    // and confirm we get a complete document on stdout. The empty-DB
    // path still emits the full skeleton (with the empty-summary line).
    let dir = tempfile::tempdir().unwrap();
    let path = dir.path().join("events.db");
    let _store = fluxmirror_store::SqliteStore::open(&path).unwrap();

    let output = Command::new(env!("CARGO_BIN_EXE_fluxmirror"))
        .args(["week", "--tz", "UTC", "--lang", "english", "--format", "html"])
        .arg("--db")
        .arg(&path)
        .output()
        .unwrap();
    assert!(
        output.status.success(),
        "non-zero exit: {}",
        String::from_utf8_lossy(&output.stderr)
    );
    let stdout = String::from_utf8(output.stdout).unwrap();
    assert!(stdout.starts_with("<!DOCTYPE html>"), "got: {}", &stdout[..50.min(stdout.len())]);
    assert!(stdout.contains("</html>"));
    assert!(stdout.contains("fluxmirror weekly digest"));
}

#[test]
fn cli_format_html_with_out_writes_file_and_prints_confirmation() {
    let dir = tempfile::tempdir().unwrap();
    let db = dir.path().join("events.db");
    let _store = fluxmirror_store::SqliteStore::open(&db).unwrap();
    let out = dir.path().join("card.html");

    let output = Command::new(env!("CARGO_BIN_EXE_fluxmirror"))
        .args(["week", "--tz", "UTC", "--lang", "english", "--format", "html"])
        .arg("--db")
        .arg(&db)
        .arg("--out")
        .arg(&out)
        .output()
        .unwrap();
    assert!(
        output.status.success(),
        "non-zero exit: {}",
        String::from_utf8_lossy(&output.stderr)
    );

    let stdout = String::from_utf8(output.stdout).unwrap();
    assert!(
        stdout.starts_with("wrote "),
        "expected 'wrote ...' confirmation, got: {stdout}"
    );
    assert!(stdout.contains("bytes)"), "missing bytes suffix: {stdout}");

    let body = std::fs::read_to_string(&out).unwrap();
    assert!(body.starts_with("<!DOCTYPE html>"));
    assert!(body.contains("</html>"));
}

#[test]
fn cli_other_reports_reject_html_with_exit_two() {
    // M5 only ships --format html for `week`. The other six reports
    // must keep returning exit 2 with a "not yet implemented" line.
    for sub in &["today", "yesterday", "agents", "compare", "about"] {
        let output = Command::new(env!("CARGO_BIN_EXE_fluxmirror"))
            .args([sub, "--lang", "english", "--format", "html"])
            .output()
            .unwrap();
        assert!(
            !output.status.success(),
            "expected exit 2 for {sub} --format html"
        );
        assert_eq!(output.status.code(), Some(2));
        let stderr = String::from_utf8(output.stderr).unwrap();
        assert!(
            stderr.contains("not yet implemented"),
            "{sub}: missing 'not yet implemented' in stderr: {stderr}"
        );
    }
}

#[test]
fn sha256_smoke_matches_well_known_inputs() {
    // Belt-and-suspenders: empty input has a hard-coded hash. Any
    // arithmetic regression in the local SHA-256 would tank both this
    // test and the snapshot hash test together.
    assert_eq!(
        sha256_hex(b""),
        "e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855"
    );
    assert_eq!(
        sha256_hex(b"abc"),
        "ba7816bf8f01cfea414140de5dae2223b00361a396177a9cb410ff61f20015ad"
    );
}
