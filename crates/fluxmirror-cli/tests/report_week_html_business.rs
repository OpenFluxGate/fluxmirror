#[test]
fn mcp_traffic_thresholds_route_to_correct_label() {
    // Build three fixtures that vary only the mcp_traffic_label and
    // assert the rendered HTML reflects each bucket's phrasing.
    let lp = pack("english");

    let mut none = business_fixture();
    none.insights.mcp_traffic_label = "none - proxy not active".into();
    let html = render_card(&none, lp);
    assert!(html.contains("MCP traffic"), "MCP heading missing in 'none'");
    assert!(html.contains("none"), "expected 'none' phrasing in MCP label");

    let mut light = business_fixture();
    light.insights.mcp_traffic_label = "light (25 msgs)".into();
    let html = render_card(&light, lp);
    assert!(html.contains("light"), "expected 'light' phrasing in MCP label");
    assert!(html.contains("25 msgs"), "expected msg count in light label");

    let mut active = business_fixture();
    active.insights.mcp_traffic_label = "active (100 msgs)".into();
    let html = render_card(&active, lp);
    assert!(html.contains("active"), "expected 'active' phrasing");
    assert!(html.contains("100 msgs"));
}

#[test]
fn weekend_only_pattern_label_localises() {
    let lp_en = pack("english");
    let mon = NaiveDate::from_ymd_opt(2026, 4, 20).unwrap();
    let sat = NaiveDate::from_ymd_opt(2026, 4, 25).unwrap();
    let sun = NaiveDate::from_ymd_opt(2026, 4, 26).unwrap();
    assert_eq!(format_active_days(&[sat, sun], lp_en), "Sat-Sun");
    let weekdays: Vec<NaiveDate> = (0..5)
        .map(|i| mon.checked_add_signed(chrono::Duration::days(i)).unwrap())
        .collect();
    assert_eq!(format_active_days(&weekdays, lp_en), "Mon-Fri");

    let lp_ko = pack("korean");
    assert_eq!(format_active_days(&[sat, sun], lp_ko), "토-일");
}

