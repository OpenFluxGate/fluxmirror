#[test]
fn theme_classifier_table_covers_all_five_outcomes() {
    let cases: &[(u32, u32, u32, DayTheme)] = &[
        (0, 0, 0, DayTheme::Idle),
        (5, 0, 0, DayTheme::Light),
        (49, 10, 5, DayTheme::Light),
        (200, 50, 20, DayTheme::Building),
        (200, 5, 30, DayTheme::Polishing),
        (1500, 80, 30, DayTheme::Shipping),
    ];
    for (calls, new, edited, want) in cases {
        let got = classify_theme(*calls, *new, *edited);
        assert_eq!(
            got, *want,
            "calls={calls}, new={new}, edited={edited}: got {:?}, want {:?}",
            got, want
        );
    }
}

#[test]
fn daily_breakdown_renders_seven_rows_with_theme_cells() {
    let lp = pack("english");
    let stats = business_fixture();
    let html = render_card(&stats, lp);
    // Section heading.
    assert!(html.contains("daily-breakdown"), "missing daily-breakdown section class");
    assert!(html.contains("Daily Breakdown"), "missing daily-breakdown heading");
    // Theme labels for the two active days.
    assert!(html.contains("building"), "missing building theme cell");
    assert!(html.contains("shipping"), "missing shipping theme cell");
    // Idle days use the idle theme color.
    assert!(html.contains("#f3f4f6"), "missing idle theme color");
    // Shipping cell uses the shipping accent.
    assert!(html.contains("#10b981"), "missing shipping color");
    // Theme suffix when ≥2 agents.
    assert!(html.contains("3 agents"), "missing per-day agent suffix");
    // Seven date rows in the table — count the date format.
    assert_eq!(
        html.matches("2026-04-").count() >= 7,
        true,
        "expected at least 7 dates in the daily breakdown table"
    );
}

