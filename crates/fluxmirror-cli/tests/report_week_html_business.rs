// M5.3 — business-grade weekly HTML card sections.
//
// Covers the four new synthesised blocks:
//   * Week Summary (3 bullets)
//   * Daily Breakdown (7-row table with theme cells)
//   * Highlights (3-5 bullets)
//   * Insights (4 bullets)
//
// All assertions are over deterministic, hand-built fixtures so the
// suite stays portable (no DB, no clock, no network). Snapshot drift
// is covered by the sibling `report_week_html` suite.

use chrono::NaiveDate;
use fluxmirror_cli::cmd::report::html::{render_card, AgentRow, WeekHtmlStats};
use fluxmirror_cli::cmd::report::week_summary::{
    classify_theme, format_active_days, DailyRow, DayTheme, Highlights, Insights, ProjectShare,
    WeekSummary,
};
use fluxmirror_core::report::pack;

/// Build a deterministic 7-row breakdown for a "weekend sprint" week
/// — Sat 800 calls, Sun 1500 calls, Mon-Fri idle.
fn weekend_sprint_breakdown() -> Vec<DailyRow> {
    let mon = NaiveDate::from_ymd_opt(2026, 4, 20).unwrap();
    let mut rows = Vec::with_capacity(7);
    for i in 0..5 {
        let d = mon.checked_add_signed(chrono::Duration::days(i)).unwrap();
        rows.push(DailyRow {
            date: d,
            dow_label: ["Mon", "Tue", "Wed", "Thu", "Fri"][i as usize].into(),
            calls: 0,
            new_files: 0,
            edited_files: 0,
            agents_active: 0,
            theme: DayTheme::Idle,
        });
    }
    rows.push(DailyRow {
        date: NaiveDate::from_ymd_opt(2026, 4, 25).unwrap(),
        dow_label: "Sat".into(),
        calls: 800,
        new_files: 60,
        edited_files: 30,
        agents_active: 3,
        theme: DayTheme::Building,
    });
    rows.push(DailyRow {
        date: NaiveDate::from_ymd_opt(2026, 4, 26).unwrap(),
        dow_label: "Sun".into(),
        calls: 1500,
        new_files: 100,
        edited_files: 63,
        agents_active: 3,
        theme: DayTheme::Shipping,
    });
    rows
}

