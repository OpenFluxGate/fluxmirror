// Self-contained HTML renderer for the M5 option A weekly digest card.
//
// One callsite (`week::run` with `--format html`) builds a `WeekHtmlStats`
// out of the existing `WeekStats` plus a couple of derived aggregates,
// then hands it to `render_card`. The output is a complete
// `<!DOCTYPE html>` document with inline `<style>` (no JS, no external
// fonts, no images, no SVG). Designed to drop straight into a Slack DM,
// Notion page, or attached `.html` file.
//
// We deliberately don't pull in askama / minijinja / handlebars: this is
// one template, and `format!` macros plus a tiny HTML escaper are
// cheaper to maintain than a build-time templating dep.

use chrono::NaiveDate;
use fluxmirror_core::report::LangPack;

use super::git_narrative::GitNarrative;

/// One row in the per-agent summary table.
#[derive(Debug, Clone)]
pub struct AgentRow {
    pub agent: String,
    pub calls: u64,
    pub sessions: u64,
    pub active_days: u64,
    pub top_tool: String,
}

/// All data the HTML card needs to render. Built by `week::run` from
/// the existing `WeekStats` collector plus a small derivation pass that
/// fills the heatmap matrix and the busiest-day / heaviest-tool fields.
#[derive(Debug, Clone)]
pub struct WeekHtmlStats {
    pub range_start: NaiveDate,
    pub range_end: NaiveDate,
    pub tz_label: String,
    /// `[day_of_week][hour_of_day]` calls. day_of_week is 0=Monday..6=Sunday.
    pub heatmap: [[u32; 24]; 7],
    pub agent_summary: Vec<AgentRow>,
    /// `(path, count)` — at most 10 rows.
    pub top_files: Vec<(String, u32)>,
    /// `(command snippet, count)` — at most 10 rows.
    pub top_shells: Vec<(String, u32)>,
    /// 0=Monday..6=Sunday — index into `lp.html_dow_labels` for the
    /// "busiest day" sentence.
    pub busiest_day_dow: u8,
    pub busiest_day_calls: u32,
    pub heaviest_tool: String,
    pub heaviest_tool_calls: u32,
    pub total_calls: u32,
    pub total_agents: usize,
    /// `Generated by fluxmirror v0.5.7 · 2026-04-27T14:12:00Z` — the
    /// caller passes a pre-formatted string so tests can pin it to a
    /// deterministic value.
    pub generated_footer: String,
    /// "Shipped this week" git narrative. `None` when collection was
    /// disabled via `--no-git-narrative`; `Some(empty)` when the pass
    /// ran but no commits landed in any cwd's repo. The renderer omits
    /// the section in both cases.
    pub narrative: Option<GitNarrative>,
}

/// Escape a string for safe HTML text/attribute interpolation.
///
/// Covers the five entities required by HTML5 in normal text + attribute
/// contexts: `& < > " '`. Single-pass, allocates only when escaping is
/// needed. Anything more elaborate (control chars, NBSP, etc.) isn't
/// in our threat model — this output is rendered locally from data the
/// user themselves recorded.
pub fn html_escape(s: &str) -> String {
    let needs_escape = s
        .bytes()
        .any(|b| matches!(b, b'&' | b'<' | b'>' | b'"' | b'\''));
    if !needs_escape {
        return s.to_string();
    }
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        match ch {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '>' => out.push_str("&gt;"),
            '"' => out.push_str("&quot;"),
            '\'' => out.push_str("&#39;"),
            other => out.push(other),
        }
    }
    out
}

/// Replace every `{name}` placeholder in `template` from the `vars`
/// `(key, value)` slice. Tiny on purpose — we only need this for the
/// summary sentence and we don't want to bring in `strfmt` or similar
/// for one render.
pub fn replace_all(template: &str, vars: &[(&str, &str)]) -> String {
    let mut out = template.to_string();
    for (k, v) in vars {
        let placeholder = format!("{{{}}}", k);
        out = out.replace(&placeholder, v);
    }
    out
}

/// Bucket a per-cell call count to one of five color steps. Returns the
/// inline `background:` value for the cell. `0` is rendered as a
/// near-transparent gray so the grid shape stays visible without
/// suggesting activity. The accent color is a single blue (#3b82f6) at
/// four lightness steps.
fn heatmap_color(count: u32) -> &'static str {
    match count {
        0 => "#f3f4f6",       // gray-100 (empty cell)
        1..=2 => "#dbeafe",   // blue-100
        3..=5 => "#93c5fd",   // blue-300
        6..=20 => "#3b82f6",  // blue-500 (accent)
        _ => "#1d4ed8",       // blue-700 (heavy)
    }
}

/// Render the entire HTML card. Returns a complete document starting
/// with `<!DOCTYPE html>`.
pub fn render_card(stats: &WeekHtmlStats, lp: &LangPack) -> String {
    // Pre-allocate roughly enough room for the document; the heatmap is
    // 168 cells and dominates the size.
    let mut out = String::with_capacity(16 * 1024);

    let title = html_escape(lp.html_title);
    let range_line = format!(
        "{} - {} &middot; {}",
        stats.range_start.format("%b %d"),
        stats.range_end.format("%b %d, %Y"),
        html_escape(&stats.tz_label)
    );

    out.push_str("<!DOCTYPE html>\n");
    out.push_str("<html lang=\"en\">\n");
    out.push_str("<head>\n");
    out.push_str("<meta charset=\"utf-8\">\n");
    out.push_str("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n");
    out.push_str(&format!("<title>{}</title>\n", title));
    out.push_str("<style>\n");
    out.push_str(INLINE_CSS);
    out.push_str("</style>\n");
    out.push_str("</head>\n");
    out.push_str("<body>\n");
    out.push_str("<main class=\"card\">\n");

    // Header.
    out.push_str("<header class=\"hd\">\n");
    out.push_str(&format!("<h1>{}</h1>\n", title));
    out.push_str(&format!("<p class=\"sub\">{}</p>\n", range_line));
    out.push_str("</header>\n");

    // Heatmap section.
    render_heatmap(&mut out, stats, lp);

    // Top files / top shells two-column block.
    out.push_str("<section class=\"two-col\">\n");
    render_top_table(
        &mut out,
        lp.html_top_files_heading,
        lp.html_item_count_columns,
        &stats.top_files,
    );
    render_top_table(
        &mut out,
        lp.html_top_shells_heading,
        lp.html_item_count_columns,
        &stats.top_shells,
    );
    out.push_str("</section>\n");

    // Per-agent summary.
    render_agent_table(&mut out, stats, lp);

    // "Shipped this week" git narrative — sits between the data-only
    // sections and the summary so the human signal lands close to the
    // closing paragraph.
    render_narrative(&mut out, stats, lp);

    // Summary paragraph.
    render_summary(&mut out, stats, lp);

    // Footer.
    out.push_str(&format!(
        "<footer class=\"ft\">{}</footer>\n",
        html_escape(&stats.generated_footer)
    ));
    out.push_str("</main>\n");
    out.push_str("</body>\n");
    out.push_str("</html>\n");
    out
}

fn render_heatmap(out: &mut String, stats: &WeekHtmlStats, lp: &LangPack) {
    out.push_str("<section class=\"hm\">\n");
    out.push_str(&format!(
        "<h2>{}</h2>\n",
        html_escape(lp.html_heatmap_heading)
    ));
    out.push_str(&format!(
        "<p class=\"axis\">{}</p>\n",
        html_escape(lp.html_hour_axis_label)
    ));
    out.push_str("<div class=\"grid\">\n");

    // Header row: empty corner cell + 24 hour columns.
    out.push_str("<div class=\"hcell hdr\"></div>\n");
    for h in 0..24u32 {
        out.push_str(&format!("<div class=\"hcell hdr\">{:02}</div>\n", h));
    }

    // Seven rows.
    for dow in 0..7usize {
        let label = lp.html_dow_labels[dow];
        out.push_str(&format!(
            "<div class=\"hcell rowlbl\">{}</div>\n",
            html_escape(label)
        ));
        for h in 0..24usize {
            let n = stats.heatmap[dow][h];
            let bg = heatmap_color(n);
            // The data attribute lets curious power users still grok the
            // grid via devtools without us having to ship JS.
            out.push_str(&format!(
                "<div class=\"hcell\" style=\"background:{};\" data-count=\"{}\" title=\"{} {:02}:00 - {} calls\"></div>\n",
                bg,
                n,
                html_escape(label),
                h,
                n
            ));
        }
    }

    out.push_str("</div>\n");
    out.push_str("</section>\n");
}

fn render_top_table(
    out: &mut String,
    heading: &str,
    cols: [&'static str; 2],
    rows: &[(String, u32)],
) {
    out.push_str("<section class=\"tt\">\n");
    out.push_str(&format!("<h2>{}</h2>\n", html_escape(heading)));
    if rows.is_empty() {
        out.push_str("<p class=\"empty\">&mdash;</p>\n");
        out.push_str("</section>\n");
        return;
    }
    out.push_str("<table>\n<thead><tr>\n");
    out.push_str(&format!(
        "<th>{}</th><th class=\"num\">{}</th>\n",
        html_escape(cols[0]),
        html_escape(cols[1])
    ));
    out.push_str("</tr></thead>\n<tbody>\n");
    for (item, count) in rows.iter().take(10) {
        out.push_str(&format!(
            "<tr><td class=\"item\">{}</td><td class=\"num\">{}</td></tr>\n",
            html_escape(item),
            count
        ));
    }
    out.push_str("</tbody>\n</table>\n");
    out.push_str("</section>\n");
}

fn render_agent_table(out: &mut String, stats: &WeekHtmlStats, lp: &LangPack) {
    out.push_str("<section class=\"ag\">\n");
    out.push_str(&format!(
        "<h2>{}</h2>\n",
        html_escape(lp.html_per_agent_heading)
    ));
    if stats.agent_summary.is_empty() {
        out.push_str("<p class=\"empty\">&mdash;</p>\n");
        out.push_str("</section>\n");
        return;
    }
    out.push_str("<table>\n<thead><tr>\n");
    for c in lp.html_agent_columns {
        out.push_str(&format!("<th>{}</th>\n", html_escape(c)));
    }
    out.push_str("</tr></thead>\n<tbody>\n");
    for row in &stats.agent_summary {
        out.push_str(&format!(
            "<tr><td>{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td class=\"num\">{}</td><td>{}</td></tr>\n",
            html_escape(&row.agent),
            row.calls,
            row.sessions,
            row.active_days,
            html_escape(&row.top_tool),
        ));
    }
    out.push_str("</tbody>\n</table>\n");
    out.push_str("</section>\n");
}

/// Render the "Shipped this week" git-narrative section.
///
/// Omits the entire `<section>` when the narrative is missing or empty
/// — the visual rule is "if there's nothing to ship, don't tease".
fn render_narrative(out: &mut String, stats: &WeekHtmlStats, lp: &LangPack) {
    let narrative = match stats.narrative.as_ref() {
        Some(n) if !n.repos.is_empty() => n,
        _ => return,
    };

    out.push_str("<section class=\"narrative\">\n");
    out.push_str(&format!(
        "<h2>{}</h2>\n",
        html_escape(lp.html_narrative_heading)
    ));
    for repo in &narrative.repos {
        let count_label = if repo.total_commits == 1 {
            lp.html_narrative_count_one.to_string()
        } else {
            lp.html_narrative_count_many
                .replace("{n}", &repo.total_commits.to_string())
        };
        out.push_str("<article>\n");
        out.push_str("<header>\n");
        out.push_str(&format!(
            "<span class=\"repo-name\">&#128193; {}</span>\n",
            html_escape(&repo.repo_name)
        ));
        out.push_str(&format!(
            "<span class=\"commit-count\">{}</span>\n",
            html_escape(&count_label)
        ));
        out.push_str("</header>\n");
        out.push_str("<ul>\n");
        for subject in &repo.commits {
            out.push_str(&format!("<li>{}</li>\n", html_escape(subject)));
        }
        out.push_str("</ul>\n");
        out.push_str("</article>\n");
    }
    out.push_str("</section>\n");
}

fn render_summary(out: &mut String, stats: &WeekHtmlStats, lp: &LangPack) {
    out.push_str("<section class=\"sm\">\n");
    out.push_str(&format!(
        "<h2>{}</h2>\n",
        html_escape(lp.html_summary_heading)
    ));
    let body = if stats.total_calls == 0 {
        lp.html_summary_empty.to_string()
    } else {
        let dow_label = lp
            .html_dow_labels
            .get(stats.busiest_day_dow as usize)
            .copied()
            .unwrap_or("?");
        let calls_str = stats.total_calls.to_string();
        let agents_str = stats.total_agents.to_string();
        let dow_calls_str = stats.busiest_day_calls.to_string();
        let tool_calls_str = stats.heaviest_tool_calls.to_string();
        replace_all(
            lp.html_summary_template,
            &[
                ("calls", calls_str.as_str()),
                ("agents", agents_str.as_str()),
                ("dow", dow_label),
                ("dow_calls", dow_calls_str.as_str()),
                ("tool", stats.heaviest_tool.as_str()),
                ("tool_calls", tool_calls_str.as_str()),
            ],
        )
    };
    out.push_str(&format!("<p>{}</p>\n", html_escape(&body)));
    out.push_str("</section>\n");
}

