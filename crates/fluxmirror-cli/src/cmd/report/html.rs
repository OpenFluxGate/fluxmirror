    #[test]
    fn render_card_narrative_escapes_commit_subjects() {
        use crate::cmd::report::git_narrative::{GitNarrative, RepoCommits};
        let lp = fluxmirror_core::report::pack("english");
        let mut stats = fixture_stats();
        stats.narrative = Some(GitNarrative {
            repos: vec![RepoCommits {
                repo_name: "evil<repo>".into(),
                repo_path: std::path::PathBuf::from("/tmp/evil"),
                commits: vec!["feat: <script>alert(1)</script>".into()],
                total_commits: 1,
            }],
        });
        let s = render_card(&stats, lp);
        assert!(!s.contains("<script>alert"), "raw script leaked");
        assert!(s.contains("&lt;script&gt;"), "expected escaped form");
        assert!(s.contains("evil&lt;repo&gt;"));
    }
